def route_query(user_query: str) -> dict:
    """
    –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è —Ä–æ—É—Ç–∏–Ω–≥–∞ - –∫–ª–∞—Å—Å–∏—Ñ–∏—Ü–∏—Ä—É–µ—Ç –∑–∞–ø—Ä–æ—Å –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π –æ—Ç–≤–µ—Ç
    
    Args:
        user_query: –ó–∞–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        
    Returns:
        –°–ª–æ–≤–∞—Ä—å —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –æ–±—Ä–∞–±–æ—Ç–∫–∏
    """
    intent = classify_intent(user_query)
    
    result = {
        "intent": intent,
        "query": user_query
    }
    
    if intent == "simple_faq":
        # –î–ª—è –ø—Ä–æ—Å—Ç—ã—Ö FAQ –¥–∞–µ–º –±—ã—Å—Ç—Ä—ã–π –æ—Ç–≤–µ—Ç
        result["answer"] = cheap_faq_answer(user_query)
        result["type"] = "faq"
    elif intent == "capabilities":
        # –î–ª—è –∑–∞–ø—Ä–æ—Å–æ–≤ –æ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—è—Ö –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–π
        result["answer"] = get_capabilities_answer()
        result["type"] = "capabilities"
    else:
        # –î–ª—è get_file –∏ complex –ø–µ—Ä–µ–¥–∞–µ–º –¥–∞–ª—å—à–µ –≤ –ø–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫
        result["needs_planning"] = True
        result["type"] = "planning_required"
    
    return result
